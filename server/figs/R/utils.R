rewrite_params <- function(params) {
  params %>% str_replace_all(
    c(
      "2x2"="2$^2$",
      "3x3"="3$^2$",
      "4x4"="4$^2$",
      "8x8"="8$^2$",
      "10x10"="10$^2$",
      "16x16"="16$^2$",
      "18x18"="18$^2$",
      "20x20"="20$^2$",
      "x"="$\\\\times$",
      "_"="\n"
    ))
}
